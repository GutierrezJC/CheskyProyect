    private void agregarProceso() {
        int idProceso = Integer.parseInt(campoNombre.getText());
        int tiempoLlegada = Integer.parseInt(campoTiempoLlegada.getText());
        int tiempoRafaga = Integer.parseInt(campoTiempoRafaga.getText());
        int prioridad = Integer.parseInt(campoNivelPrioridad.getText());
        int tamanomemoria = Integer.parseInt(campoMemoria.getText());
        String nombres = "Proceso" + String.valueOf(idProceso);

        // Proceso(int idProceso, String nombre, int tama√±oEnBytes, int tama√±oPagina, int tiempoLlegada, int tiempoRafaga, int prioridad,int maxMarcos)
        Proceso proceso = new Proceso(idProceso, nombres, tamanomemoria, 100, tiempoLlegada, tiempoRafaga, prioridad, 5);
//       Proceso proceso = new Proceso(idProceso, tiempoLlegada, tiempoRafaga, prioridad);
        procesos.add(proceso);
        listaProcesosModelo.addElement(proceso.getIdProceso() + " - Llegada: " + tiempoLlegada + ", R√°faga: " + tiempoRafaga + ", Prioridad: " + prioridad);

        campoNombre.setText("");
        campoTiempoLlegada.setText("");
        campoTiempoRafaga.setText("");
        campoNivelPrioridad.setText("");
        campoMemoria.setText("");
    }

